class Event:

    def __init__(self, year,month,day,hour,minutes,description):
        #Year of the event
        self.year = year
        #Month of the event
        self.month = month
        #Day of the event
        self.day = day
        #hour of the event
        self.hour = hour
        #minutes of the event
        self.minutes = minutes
        #descripion of the event
        self.description = description